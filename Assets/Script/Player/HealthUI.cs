using UnityEngine;
using UnityEngine.UI;

public class HealthUI : MonoBehaviour
{
    public HealthData healthData;
    public Image healthFillImage; 
    private void OnEnable()
    {
        healthData.onHealthChanged.AddListener(UpdateUI);
    }

    private void OnDisable()
    {
        healthData.onHealthChanged.RemoveListener(UpdateUI);
    }

    void Start()
    {
        UpdateUI(); 
    }

    void UpdateUI()
    {
        float fillAmount = (float)healthData.currentHealth / healthData.maxHealth;
        healthFillImage.fillAmount = fillAmount;
    }
}
